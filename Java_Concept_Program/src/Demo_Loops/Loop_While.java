package Demo_Loops;

public class Loop_While {
	
	
	public static void main(String[] args) {
	
				//initilization
				//conditon
				//statement
				//iteration
			
			int num = 5;
			System.out.println("start");
			while (num>=3 ) {
				
				System.out.print(num+" ");
				num--;
			}
			System.out.println();
			System.out.println("end");
	}
	
	
			
	
				
			
}
